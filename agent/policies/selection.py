from __future__ import annotations

from agent.models.task import TaskMode
from agent.policies.generic import GenericPolicy


class SelectionPolicy(GenericPolicy):
    mode = TaskMode.SELECTION

    def mode_rules(self) -> str:
        return (
            "Сопоставь текст цели подзадачи с тем, что видно в списке элементов: какой смысл действия нужен "
            "(фильтр, вариант, количество, переключатель и т.д.) — выводи из цели и из role/name/state у узлов, "
            "без предположений о фиксированных URL или типичных подписях кнопок конкретных сайтов. "
            "Если для одного и того же объекта в наблюдении есть и крупная ссылка/карточка с длинным текстом, "
            "и отдельный узкий контрол (button, checkbox, radio, spinbutton, menuitem и т.п.) с ясной семантикой, "
            "для шага из цели предпочитай контрол, а не клик по всей карточке или только по длинной подписи-ссылке, "
            "когда специализированный контрол уже есть в списке. "
            "Нужного элемента нет в области просмотра — scroll и перечитай элементы. "
            "Когда по цели и текущему UI цель достигнута, заверши подзадачу (finish), не повторяя тот же шаг."
        )

