from __future__ import annotations

from agent.models.task import TaskMode
from agent.policies.generic import GenericPolicy


class TransactionPolicy(GenericPolicy):
    mode = TaskMode.TRANSACTION

    def mode_rules(self) -> str:
        return (
            "Разделяй по цели и по observation: выбор строки/позиции в списке, изменение числа, "
            "промежуточное подтверждение шага и финальные денежные действия — опирайся на role, name и контекст "
            "соседних элементов, а не на заранее заданные формулировки подписей или пути страницы. "
            "Если рядом с позицией есть отдельные контролы для количества или для целевого действия по смыслу цели, "
            "предпочитай их клику по всей карточке или только по длинной ссылке-названию, когда узкий контрол уже в списке. "
            "Нужных контролов не видно — scroll и перечитай элементы. "
            "Опасные шаги помечай is_dangerous=true; до подтверждения пользователя не выполняй "
            "окончательные платежи и оформление заказа."
        )

