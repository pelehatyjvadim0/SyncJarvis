def planner_plan_rules_block(max_subtasks: int) -> str:
    return (
        "- Не добавляй подзадачи, которых нет в запросе пользователя: никаких «на всякий случай», проверок качества, "
        "регистрации, оплаты, доставки, отзывов и т.п., если пользователь этого явно не просил.\n"
        "- Держи план коротким: предпочитай минимально достаточное число шагов (часто 1–3), не раздувай список до лимита без нужды.\n"
        "- Ровно одна атомарная цель на подзадачу: в одном goal не объединяй две независимые задачи "
        "(не пиши «сделай A и B» в одной строке). Союзы «и», «затем», «после чего» между разными действиями — "
        "разносить на отдельные элементы subtasks с разными goal.\n"
        "- В каждом goal формулируй одно конкретное действие или результат, напрямую вытекающий из текста пользователя; "
        "избегай размытых формулировок вроде «убедиться что всё работает» или «завершить процесс» без привязки к запросу.\n"
        "- Поле title — краткая суть шага (не дублируй целиком goal).\n"
        f"- Не больше {max_subtasks} подзадач.\n\n"
        'Пример корректного начала ответа: {"thought":"...","subtasks":[...]}\n\n'
    )


