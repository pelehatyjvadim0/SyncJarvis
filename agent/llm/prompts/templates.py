    return (
        "Формат ответа (единые ограничения):\n"
        "- Только один валидный JSON-объект: без markdown, без кодовых блоков и без обёрток вида ``` или ```json.\n"
        "- Не добавляй пояснительный текст, комментарии или префиксы до открывающей «{» и после закрывающей «}».\n"
        "- Ответ должен начинаться с символа «{» и заканчиваться «}».\n\n"
    )


